package application.dto;

public class OptionInputDTO {

    private String width;
    private String height;
    private String textSize;
    private String textColor;
    private String glowValue;
    private String glowColor;
    private String outlineValue;
    private String outlineColor;
    private String backgroundColor;

    public OptionInputDTO() {
    }


    public String getWidth() {
        return width;
    }

    public void setWidth(String width) {
        this.width = width;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getTextSize() {
        return textSize;
    }

    public void setTextSize(String textSize) {
        this.textSize = textSize;
    }

    public String getTextColor() {
        return textColor;
    }

    public void setTextColor(String textColor) {
        this.textColor = textColor;
    }

    public String getGlowValue() {
        return glowValue;
    }

    public void setGlowValue(String glowValue) {
        this.glowValue = glowValue;
    }

    public String getGlowColor() {
        return glowColor;
    }

    public void setGlowColor(String glowColor) {
        this.glowColor = glowColor;
    }

    public String getOutlineValue() {
        return outlineValue;
    }

    public void setOutlineValue(String outlineValue) {
        this.outlineValue = outlineValue;
    }

    public String getOutlineColor() {
        return outlineColor;
    }

    public void setOutlineColor(String outlineColor) {
        this.outlineColor = outlineColor;
    }

    public String getBackgroundColor() {
        return backgroundColor;
    }

    public void setBackgroundColor(String backgroundColor) {
        this.backgroundColor = backgroundColor;
    }
}
